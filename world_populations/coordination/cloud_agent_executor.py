if __name__ == "__main__":
    project_root = Path(__file__).resolve().parents[1]

    endpoint = CloudAgentEndpoint(project_root)

    # Check if called with arguments
    if len(sys.argv) > 1:
        if sys.argv[1] == "status":
            result = endpoint.get_status()
        elif sys.argv[1] == "execute":
            result = endpoint.invoke()
        else:
            result = {"error": f"Unknown command: {sys.argv[1]}"}
    else:
        # Default: execute pipeline
        result = endpoint.invoke()

    # Output result as JSON
    print("\n" + "=" * 70)
    print("CLOUD AGENT RESULT")
    print("=" * 70)
    print(json.dumps(result, indent=2))

    # Save result for local agent pickup
    result_file = (
        project_root
        / "coordination"
        / "cloud_results"
        / f"cloud_result_{datetime.now().strftime('%Y%m%d_%H%M%S')}.json"
    )
    result_file.parent.mkdir(parents=True, exist_ok=True)

    with open(result_file, "w") as f:
        json.dump(result, f, indent=2)

    print(f"\nResults saved to: {result_file}")
